class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('preferredLanguage') || 'es';
        this.init();
    }

    init() {
        this.updateDOM();
        this.updateActiveButtons();
        this.bindEvents();
    }

    t(key) {
        return locales[this.currentLang]?.[key] || key;
    }

    setLanguage(lang) {
        if (locales[lang]) {
            this.currentLang = lang;
            localStorage.setItem('preferredLanguage', lang);
            this.updateDOM();
            this.updateActiveButtons();
        }
    }

    updateDOM() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                el.textContent = translation;
            }
        });

        // Update title if exists
        const pageTitleKey = document.querySelector('title')?.getAttribute('data-i18n');
        if (pageTitleKey) {
            document.title = this.t(pageTitleKey);
        }

        // Update meta description if exists
        const metaDesc = document.querySelector('meta[name="description"]');
        const metaDescKey = metaDesc?.getAttribute('data-i18n');
        if (metaDescKey) {
            metaDesc.setAttribute('content', this.t(metaDescKey));
        }

        document.documentElement.lang = this.currentLang;
    }

    updateActiveButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.id === `lang-${this.currentLang}`);
        });
    }

    bindEvents() {
        document.getElementById('lang-es').addEventListener('click', () => this.setLanguage('es'));
        document.getElementById('lang-en').addEventListener('click', () => this.setLanguage('en'));
    }
}

document.addEventListener('DOMContentLoaded', () => {
    window.i18n = new I18n();
});
